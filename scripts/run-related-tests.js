#!/usr/bin/env node

const { execSync, spawnSync } = require('child_process');
const fs = require('fs');
const path = require('path');

const argv = process.argv.slice(2);

const getArgValue = (name, fallback) => {
  const prefix = `${name}=`;
  const found = argv.find((arg) => arg.startsWith(prefix));
  return found ? found.slice(prefix.length) : fallback;
};

const mode = getArgValue('--mode', 'pending');
const thresholdRaw = getArgValue('--coverage-threshold', '80');
const coverageThreshold = Number(thresholdRaw);

if (Number.isNaN(coverageThreshold) || coverageThreshold < 0 || coverageThreshold > 100) {
  console.error(`Invalid --coverage-threshold value: ${thresholdRaw}`);
  process.exit(1);
}

const readLines = (command) => {
  try {
    return execSync(command, { encoding: 'utf8', stdio: ['pipe', 'pipe', 'ignore'] })
      .split('\n')
      .map((line) => line.trim())
      .filter(Boolean);
  } catch (error) {
    return [];
  }
};

const unique = (items) => [...new Set(items)];

const getPendingFiles = () => {
  const changedFromHead = readLines('git diff --name-only --diff-filter=ACMR HEAD');
  const untracked = readLines('git ls-files --others --exclude-standard');
  return unique([...changedFromHead, ...untracked]);
};

const getStagedFiles = () => readLines('git diff --cached --name-only --diff-filter=ACMR');

const isTestableSource = (file) => /^(apps|packages)\//.test(file)
  && /\.(js|jsx|ts|tsx)$/.test(file)
  && !/\.test\.(js|jsx|ts|tsx)$/.test(file)
  && !/\.spec\.(js|jsx|ts|tsx)$/.test(file);

const selectedFiles = mode === 'staged' ? getStagedFiles() : getPendingFiles();
const testableFiles = unique(selectedFiles.filter(isTestableSource));

if (testableFiles.length === 0) {
  console.log(`[tests:${mode}] No testable changed files detected.`);
  process.exit(0);
}

console.log(`[tests:${mode}] Running related tests for:`);
testableFiles.forEach((file) => console.log(` - ${file}`));

const jestArgs = [
  'jest',
  '--findRelatedTests',
  ...testableFiles,
  '--coverage',
  '--passWithNoTests',
  '--coverageReporters',
  'json-summary',
  '--coverageReporters',
  'text-summary',
  ...testableFiles.flatMap((file) => ['--collectCoverageFrom', file]),
];

const result = spawnSync('npx', jestArgs, {
  stdio: 'inherit',
  shell: true,
});

if (result.status === null || result.status !== 0) {
  process.exit(result.status === null ? 1 : result.status);
}

const metricsToCheck = ['branches', 'functions', 'lines', 'statements'];
const coverageSummaryPath = path.join(process.cwd(), 'coverage', 'coverage-summary.json');

if (
  !fs.existsSync(coverageSummaryPath)
) {
  console.error('[coverage] coverage-summary.json was not generated by Jest.');
  process.exit(1);
}

const coverageSummary = JSON.parse(fs.readFileSync(coverageSummaryPath, 'utf8'));
const coverageEntries = Object.entries(coverageSummary)
  .filter(([filePath]) => filePath !== 'total')
  .map(([filePath, metrics]) => ({
    filePath: filePath.replace(/\\/g, '/'),
    metrics,
  }));

const failures = [];

testableFiles.forEach((changedFile) => {
  const normalizedChangedFile = changedFile.replace(/\\/g, '/');
  const coverageEntry = coverageEntries.find(
    ({ filePath }) => filePath.endsWith(normalizedChangedFile),
  );

  if (!coverageEntry) {
    failures.push(`${changedFile} -> no coverage information was produced`);
    return;
  }

  metricsToCheck.forEach((metric) => {
    const pct = coverageEntry.metrics[metric]?.pct;

    if (typeof pct !== 'number' || pct < coverageThreshold) {
      failures.push(
        `${changedFile} -> ${metric} ${typeof pct === 'number' ? pct : 'N/A'}% (required ${coverageThreshold}%)`,
      );
    }
  });
});

if (failures.length > 0) {
  console.error(`\n[coverage] Failed threshold check (${coverageThreshold}%) for changed files:`);
  failures.forEach((failure) => console.error(` - ${failure}`));
  process.exit(1);
}

console.log(`\n[coverage] All changed files meet the ${coverageThreshold}% threshold.`);
